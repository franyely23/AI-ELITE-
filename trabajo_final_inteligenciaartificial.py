"""**Entrenamiento del machine learning**



>Prepara los datos, codifica las variables categóricas, entrena dos árboles de decisión (prioridad y tiempo estimado) y evalúa su rendimiento.


"""

#COLUMNAS QUE UTILIZARÁ EL MODELO
columnas_entrenamiento = [
    "categoria",
    "area",
    "urgencia",
    "impacto",
    "tiempo_estimado_min",
    "canal",
    "tipo_usuario",
    "dispositivo",
    "horario",
    "prioridad",
]

# Eliminamos filas con valores nulos para evitar errores durante el entrenamiento
datos_entrenamiento = dataset[columnas_entrenamiento].dropna().copy()

#CODIFICACIÓN DE VARIABLES CATEGÓRICAS
codificadores = {}

columnas_categoricas = [
    "categoria",
    "area",
    "urgencia",
    "impacto",
    "canal",
    "tipo_usuario",
    "dispositivo",
    "horario",
]

for nombre_columna in columnas_categoricas:

    codificador = sklearn.preprocessing.LabelEncoder()

    datos_entrenamiento[nombre_columna] = codificador.fit_transform(
        datos_entrenamiento[nombre_columna].astype(str)
    )

    codificadores[nombre_columna] = codificador

#VARIABLES DE ENTRADA (X)
variables_entrada = datos_entrenamiento[columnas_categoricas]

#VARIABLES OBJETIVO (Y)
objetivo_prioridad = dataset.loc[
    datos_entrenamiento.index,
    "prioridad",
]

objetivo_tiempo = datos_entrenamiento["tiempo_estimado_min"]


#DIVISIÓN DE DATOS Y ENTRENAMIENTO DEL MODELO DE PRIORIDAD
