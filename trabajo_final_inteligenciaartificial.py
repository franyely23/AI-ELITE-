"""**Funsion buscar ticket**

>Solicita un número de ticket, valida la entrada, busca el ticket en el dataset y muestra su información si existe; permite salir al menú principal.


"""

def buscar_tickets():
    while True:
        numero = input("\nIngrese el número de ticket a buscar (o escribe 'salir' para volver): ")

        if numero.lower() == 'salir' or numero == '6':
            print("\nRegresando al menú principal...")
            break

        # Validamos si la entrada es numérica
        if not numero.isdigit():
            print(f"Error de entrada: '{numero}' contiene letras o caracteres no válidos.")
            print("Por favor, introduce solo dígitos numéricos (ej. 1001). Inténtalo de nuevo.")
            continue

        # Si pasó la validación, lo convertimos con a entero
        numero_ticket = int(numero)

        # Búsqueda en el dataset
        coincidencia = dataset[dataset['ticket'] == numero_ticket]

        # Verificamos si encontramos el ticket
        if not coincidencia.empty:
            print("\n" + "="*40)
            print(f"INFORMACIÓN DEL TICKET: {numero_ticket}")
            print("="*40)

            ticket_datos = coincidencia.iloc[0]
            for clave, valor in ticket_datos.items():
                # Saltamos la columna 'ticket' para no repetirla en el reporte
                if clave == 'ticket':
                    continue

                # Formateamos el nombre de las columnas (cambiamos '_' por espacios y capitalizamos)
                columna_formateada = str(clave).replace('_', ' ').capitalize()
                print(f"{columna_formateada:<22}: {valor}")

            print("="*40)
            break

        else:
            print(f"Buscado: Ticket {numero_ticket} No encontrado en el sistema. Inténtalo de nuevo.")

